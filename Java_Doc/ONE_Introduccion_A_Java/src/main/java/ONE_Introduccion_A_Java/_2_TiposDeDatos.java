package src.main.java.ONE_Introduccion_A_Java;

import java.lang.reflect.Type;
import java.lang.reflect.TypeVariable;

public class _2_TiposDeDatos {

    public static void main(String[] args) {
        // Tipos de datos primitivos
        // Enteros
        byte enteroByte = 127; // 8 bits
        short enteroShort = 32767; // 16 bits
        int enteroInt = 2147483647; // 32 bits
        long enteroLong = 9223372036854775807L; // 64 bits

        // Decimales
        float decimalFloat = 3.4028235E38f; // 32 bits
        double decimalDouble = 1.7976931348623157E308; // 64 bits

        // Caracteres
        char caracter = 'A'; // 16 bits

        // Booleanos
        boolean booleano = true; // 1 bit

        // Tipos de datos no primitivos
        // String
        String cadena = "Hola mundo";

        // Clases
        Object objeto = new Object();

        // Imprimir los valores
        System.out.println("Enteros");

    }


}
